  const STORAGE_KEY = "bfc24_lang";
  const DEFAULT_LANG = "ru";
  const SUPPORTED_LANGS = ["ru", "en", "zh"];

  function normalizeLang(lang) {
    return SUPPORTED_LANGS.includes(lang) ? lang : DEFAULT_LANG;
  }

  function getLang() {
    const saved = localStorage.getItem(STORAGE_KEY);
    return normalizeLang(saved || DEFAULT_LANG);
  }

  function setLang(lang) {
    const normalized = normalizeLang(lang);
    localStorage.setItem(STORAGE_KEY, normalized);
    applyTranslations();
    document.dispatchEvent(new CustomEvent("app:language-changed", { detail: { lang: normalized } }));
  }

  function getValueByPath(obj, path) {
    return String(path || "")
      .split(".")
      .reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), obj);
  }

  function interpolate(template, params = {}) {
    return String(template).replace(/\{(\w+)\}/g, (_, key) => {
      return params[key] !== undefined && params[key] !== null ? String(params[key]) : `{${key}}`;
    });
  }

  function t(path, fallback = "") {
    const dict = dictionaries[getLang()] || dictionaries[DEFAULT_LANG];
    const value = getValueByPath(dict, path);
    if (value !== undefined) return value;
    const defaultValue = getValueByPath(dictionaries[DEFAULT_LANG], path);
    if (defaultValue !== undefined) return defaultValue;
    return fallback || path;
  }

  function format(path, params = {}, fallback = "") {
    return interpolate(t(path, fallback), params);
  }

  function applyTranslations(root = document) {
    root.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (!key) return;
      el.textContent = t(key);
    });

    root.querySelectorAll("[data-i18n-html]").forEach((el) => {
      const key = el.getAttribute("data-i18n-html");
      if (!key) return;
      el.innerHTML = t(key);
    });

    root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (!key) return;
      el.setAttribute("placeholder", t(key));
    });

    root.querySelectorAll("[data-i18n-title]").forEach((el) => {
      const key = el.getAttribute("data-i18n-title");
      if (!key) return;
      el.setAttribute("title", t(key));
    });

    root.querySelectorAll("[data-page-title-i18n]").forEach((el) => {
      const key = el.getAttribute("data-page-title-i18n");
      if (!key) return;
      document.title = t(key);
    });

    root.querySelectorAll("[data-lang]").forEach((el) => {
      const code = normalizeLang(el.getAttribute("data-lang"));
      el.classList.toggle("active", code === getLang());
    });

    const titleKey = document.documentElement.getAttribute("data-page-title-i18n");
    if (titleKey) document.title = t(titleKey);

    document.documentElement.setAttribute("lang", getLang());
  }

  function renderLanguageSwitcher(target) {
    const container = typeof target === "string" ? document.getElementById(target) : target;
    if (!container) return;

    container.innerHTML = `
      <button type="button" class="btn btn-light" data-lang="ru">${t("common.languageRu")}</button>
      <button type="button" class="btn btn-light" data-lang="en">${t("common.languageEn")}</button>
      <button type="button" class="btn btn-light" data-lang="zh">${t("common.languageZh")}</button>
    `;

    container.querySelectorAll("[data-lang]").forEach((button) => {
      button.addEventListener("click", () => {
        setLang(button.getAttribute("data-lang"));
      });
    });

    applyTranslations(container);
  }

  window.i18n = {
    getLang,
    setLang,
    t,
    format,
    applyTranslations,
    renderLanguageSwitcher
  };
